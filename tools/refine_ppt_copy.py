def rewrite_pptx(source: Path, target: Path) -> None:
    with tempfile.TemporaryDirectory(prefix="ppt-refine-") as tmp:
        tmpdir = Path(tmp)
        with zipfile.ZipFile(source) as zin:
            zin.extractall(tmpdir)

        for relative_path, replacements in REPLACEMENTS.items():
            path = tmpdir / relative_path
            content = path.read_text(encoding="utf-8")
            original = content
            for old, new in replacements.items():
                if old not in content:
                    raise ValueError(f"Missing text in {relative_path}: {old}")
                content = content.replace(old, new)
            if content != original:
                path.write_text(content, encoding="utf-8")

        with zipfile.ZipFile(target, "w", compression=zipfile.ZIP_DEFLATED) as zout:
            for path in sorted(tmpdir.rglob("*")):
                if path.is_file():
                    zout.write(path, path.relative_to(tmpdir).as_posix())


